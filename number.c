int	string_to_number(const char *str)
{
	int		n;
	char	s;

	s = *str;
	if (s == '-' || s == '+')
		str++;
	n = 0;
	while (*str >= '0' && *str <= '9')
		n = n * 10 + *(str++) - '0';
	return (s == '-' ? -n : n);
}

int	char_is_digit(int c)
{
	return (c >= '0' && c <= '9');
}
